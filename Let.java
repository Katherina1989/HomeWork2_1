package homework21;

public class Let {
    private String name;
    private int level;
    private int length;

    public Let(String name, int level, int length) {
        this.name = name;
        this.level = level;
        this.length = length;
    }

    public String getName() {
        return name;
    }

    public int getLevel() {
        return level;
    }

    public int getLength() {
        return length;
    }
}
